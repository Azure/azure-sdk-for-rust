//! Build script for `azure_data_cosmos_driver_native`.
//!
//! Two responsibilities:
//!
//! 1. Bake a `BUILD_IDENTIFIER` string into the binary so external tools can
//!    identify exact builds (`strings libazurecosmosdriver.so | grep '$Id:'`).
//! 2. Generate the C header `include/azurecosmosdriver.h` from the
//!    `#[repr(C)]` / `extern "C"` surface using cbindgen.

use std::collections::HashMap;
use std::io::Write;

fn main() {
    // ── BUILD_IDENTIFIER ────────────────────────────────────────────────────
    let build_id = format!(
        "$Id: {}, Version: {}, Commit: {}, Branch: {}, Build ID: {}, Build Number: {}, Timestamp: {}$",
        "azurecosmosdriver",
        env!("CARGO_PKG_VERSION"),
        option_env!("BUILD_SOURCEVERSION").unwrap_or("unknown"),
        option_env!("BUILD_SOURCEBRANCH").unwrap_or("unknown"),
        option_env!("BUILD_BUILDID").unwrap_or("unknown"),
        option_env!("BUILD_BUILDNUMBER").unwrap_or("unknown"),
        std::time::SystemTime::now()
            .duration_since(std::time::UNIX_EPOCH)
            .unwrap_or_default()
            .as_secs(),
    );
    println!("cargo:rustc-env=BUILD_IDENTIFIER={build_id}");

    // ── Header generation ──────────────────────────────────────────────────
    // Skip on wasm targets (no C interop there).
    if std::env::var("CARGO_CFG_TARGET_FAMILY").unwrap_or_default() == "wasm" {
        return;
    }

    let header_preamble = r"// Copyright (c) Microsoft Corporation. All rights reserved.
// Licensed under the MIT License.

// This file is auto-generated by cbindgen. Do not edit manually.
// cSpell: disable"
        .to_string();

    // cbindgen rename map: keep the public Rust type name short while
    // exposing a tidy snake_case `cosmos_*` prefix to C consumers.
    let rename: HashMap<String, String> = [
        ("RuntimeContext", "runtime"),
        ("RuntimeOptions", "runtime_options"),
        ("CallContext", "call_context"),
        ("CallContextOptions", "call_context_options"),
        ("CosmosError", "error"),
        ("CosmosErrorCode", "error_code"),
        ("CosmosBytes", "bytes"),
        ("CosmosBytesView", "bytes_view"),
    ]
    .into_iter()
    .map(|(k, v)| (k.to_string(), v.to_string()))
    .collect();

    let config = cbindgen::Config {
        language: cbindgen::Language::C,
        header: Some(header_preamble),
        after_includes: Some(format!(
            "\n// Version of azurecosmosdriver this header was generated from.\n#define AZURECOSMOSDRIVER_H_VERSION \"{}\"",
            env!("CARGO_PKG_VERSION"),
        )),
        cpp_compat: true,
        parse: cbindgen::ParseConfig {
            parse_deps: true,
            include: Some(vec!["azure_data_cosmos_driver".into()]),
            ..Default::default()
        },
        style: cbindgen::Style::Both,
        enumeration: cbindgen::EnumConfig {
            rename_variants: cbindgen::RenameRule::QualifiedScreamingSnakeCase,
            ..Default::default()
        },
        documentation_length: cbindgen::DocumentationLength::Full,
        documentation_style: cbindgen::DocumentationStyle::Doxy,
        export: cbindgen::ExportConfig {
            prefix: Some("cosmos_".into()),
            rename,
            ..Default::default()
        },
        ..Default::default()
    };

    let crate_dir = std::env::var("CARGO_MANIFEST_DIR").unwrap();
    let Ok(bindings) = cbindgen::Builder::new()
        .with_crate(crate_dir)
        .with_config(config)
        .generate()
    else {
        println!("cargo:warning=cbindgen failed to generate azurecosmosdriver.h");
        return;
    };

    // Write to a buffer first, then collapse runs of blank lines (cbindgen
    // emits extras when it skips non-`#[repr(C)]` types).
    let mut buf = Vec::new();
    bindings.write(&mut buf);
    let raw = String::from_utf8(buf).expect("cbindgen output should be UTF-8");
    let mut collapsed = String::with_capacity(raw.len());
    let mut blanks = 0u32;
    for line in raw.split('\n') {
        if line.trim().is_empty() {
            blanks += 1;
            if blanks <= 1 {
                collapsed.push('\n');
            }
        } else {
            blanks = 0;
            collapsed.push_str(line);
            collapsed.push('\n');
        }
    }
    let collapsed = format!("{}\n", collapsed.trim_end_matches('\n'));

    std::fs::create_dir_all("include").expect("failed to create include/ directory");
    let mut f = std::fs::File::create("include/azurecosmosdriver.h")
        .expect("failed to open include/azurecosmosdriver.h for writing");
    f.write_all(collapsed.as_bytes())
        .expect("failed to write include/azurecosmosdriver.h");
}
